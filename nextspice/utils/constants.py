# ==========================================
# 數值穩定性策略 (Numerical Regularization)
# ==========================================
# 1. 非線性收斂保護：加在 Diode/BJT 等效電導上，防止完全截止時 Jacobian 矩陣奇異
GMIN_NONLINEAR = 1e-12    

# 2. DC 拓樸修補：加在 Capacitor 兩端，防止 DC OP 分析時節點浮接 (Floating Node)
GMIN_DC_PULLDOWN = 1e-12  

# 3. 支路方程式補丁：加在 Inductor 等引入額外電流變數的對角線上，防止零對角線
GMIN_BRANCH_PATCH = 1e-12 

# ==========================================
# 數學運算安全邊界 (Math Safe Bounds)
# ==========================================
LOG_FLOOR = 1e-30     # 防止 math.log(0) 崩潰
EXP_LIMIT = 100.0     # 防止 math.exp() 溢位

# ==========================================
# 分析預設參數
# ==========================================
DEFAULT_DT = 1e-9     # 預設時間步長 (1ns)

# ==========================================
# 物理常數
# ==========================================
BOLTZMANN_K = 1.380649e-23   
CHARGE_Q = 1.602176634e-19